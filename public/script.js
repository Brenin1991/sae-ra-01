// Componente para fazer objetos sempre olharem para a c√¢mera
AFRAME.registerComponent('billboard', {
    tick: function () {
        const camera = document.querySelector('[camera]');
        if (camera) {
            this.el.object3D.lookAt(camera.object3D.position);
        }
    }
});

// Componente para objetos interativos
AFRAME.registerComponent('interactive-object', {
    schema: {
        objectId: {type: 'string'},
        imageSrc: {type: 'string'},
        pecaSrc: {type: 'string'}
    },
    
    init: function () {
        const el = this.el;
        const data = this.data;
        let isGlowing = false;
        
        // Eventos de clique/toque
        el.addEventListener('click', function () {
            console.log('üéØ Objeto clicado:', data.objectId);
            
            if (!isGlowing) {
                // Ativar brilho
                el.setAttribute('animation__glow', {
                    property: 'material.emissiveIntensity',
                    to: 0.8,
                    dur: 1000,
                    loop: true,
                    dir: 'alternate'
                });
                
                el.setAttribute('material', {
                    emissive: '#FF1493',
                    emissiveIntensity: 0.5
                });
                
                // Aumentar tamanho
                el.setAttribute('animation__scale', {
                    property: 'scale',
                    to: '1.2 1.2 1.2',
                    dur: 300
                });
                
                isGlowing = true;
                
                // Mostrar pe√ßa
                showPeca(data.pecaSrc);
                
            } else {
                // Voltar ao normal
                el.removeAttribute('animation__glow');
                el.setAttribute('material', {
                    emissive: '#000000',
                    emissiveIntensity: 0
                });
                
                el.setAttribute('animation__scale', {
                    property: 'scale',
                    to: '1 1 1',
                    dur: 300
                });
                
                isGlowing = false;
                
                // Esconder pe√ßa
                hidePeca();
            }
        });
        
        // Feedback visual no hover
        el.addEventListener('mouseenter', function () {
            if (!isGlowing) {
                el.setAttribute('animation__hover', {
                    property: 'scale',
                    to: '1.1 1.1 1.1',
                    dur: 200
                });
            }
        });
        
        el.addEventListener('mouseleave', function () {
            if (!isGlowing) {
                el.setAttribute('animation__hover', {
                    property: 'scale',
                    to: '1 1 1',
                    dur: 200
                });
            }
        });
    }
});

// Fun√ß√£o para mostrar pe√ßa
function showPeca(pecaSrc) {
    const modal = document.getElementById('peca-modal');
    const plane = document.getElementById('peca-plane');
    
    if (modal && plane) {
        plane.setAttribute('material', 'src', pecaSrc);
        modal.setAttribute('visible', true);
        
        // Posicionar na frente da c√¢mera
        const camera = document.querySelector('[camera]');
        if (camera) {
            const cameraPosition = camera.getAttribute('position');
            modal.setAttribute('position', {
                x: cameraPosition.x,
                y: cameraPosition.y,
                z: cameraPosition.z - 2
            });
        }
        
        console.log('üìã Mostrando pe√ßa:', pecaSrc);
    }
}

