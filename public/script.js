// Fun√ß√£o para alternar entre modo AR e HDRI
function toggleMode() {
    const video = document.getElementById('webcam');
    const sky = document.querySelector('a-sky');
    const scene = document.querySelector('a-scene');
    const button = document.getElementById('toggleMode');
    
    if (isARMode) {
        // Mudar para modo HDRI
        console.log('üåÖ Mudando para modo HDRI...');
        
        // Parar webcam
        if (currentStream) {
            currentStream.getTracks().forEach(track => track.stop());
            currentStream = null;
        }
        if (video) {
            video.srcObject = null;
            video.style.display = 'none';
        }
        
        // Ativar HDRI
        if (sky) {
            sky.setAttribute('visible', 'true');
        }
        if (scene) {
            scene.setAttribute('background', '');
        }
        
        button.textContent = 'Modo AR';
        isARMode = false;
        
        // Esconder objetos interativos em modo HDRI (opcional)
        const interactiveContainer = document.getElementById('interactive-objects');
        if (interactiveContainer) {
            interactiveContainer.setAttribute('visible', 'true'); // Manter vis√≠vel
        }
        
    } else {
        // Mudar para modo AR
        console.log('üì± Mudando para modo AR...');
        
        // Desativar HDRI
        if (sky) {
            sky.setAttribute('visible', 'false');
        }
        if (scene) {
            scene.setAttribute('background', 'transparent: true');
        }
        
        // Reativar webcam
        if (video) {
            video.style.display = 'block';
        }
        initWebcam();
        
        button.textContent = 'Modo HDRI';
        isARMode = true;
    }
}

// Sistema de detec√ß√£o autom√°tica para mobile
function startMobileDetection() {
    // Detectar se √© mobile
    const isMobile = /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent);
    
    if (isMobile) {
        console.log('üì± Mobile detectado - iniciando sistema de auto-detec√ß√£o');
        
        // Usar orienta√ß√£o da c√¢mera para detectar objetos
        const camera = document.querySelector('a-camera');
        
        // Verificar intersec√ß√µes a cada 200ms (mais responsivo)
        setInterval(() => {
            const cursor = document.querySelector('a-cursor');
            if (cursor && cursor.components && cursor.components.raycaster) {
                const intersections = cursor.components.raycaster.intersectedEls;
                
                if (intersections.length > 0) {
                    const intersectedEl = intersections[0];
                    
                    // Verificar se tem o componente interactive-object
                    if (intersectedEl.hasAttribute('interactive-object')) {
                        const component = intersectedEl.components['interactive-object'];
                        if (component && !intersectedEl.alreadyTriggeredMobile) {
                            console.log('üì± Auto-detec√ß√£o m√≥vel:', component.data.objectId);
                            intersectedEl.alreadyTriggeredMobile = true;
                            
                            // Disparar diretamente a fun√ß√£o de criar pe√ßa
                            const showPecaFunction = intersectedEl.showPecaOnIntersection;
                            if (showPecaFunction) {
                                showPecaFunction();
                            }
                        }
                    }
                }
            }
        }, 200); // Verificar a cada 200ms
    }
}

// Fun√ß√£o para adicionar detec√ß√£o via movimento do dispositivo
function addDeviceOrientationDetection() {
    // Para mobile: quando mexer o celular, resetar flags para permitir nova detec√ß√£o
    if ('DeviceOrientationEvent' in window) {
        let lastCheck = 0;
        
        window.addEventListener('deviceorientation', function() {
            const now = Date.now();
            if (now - lastCheck > 1000) { // A cada 1 segundo
                // Resetar flags para permitir nova detec√ß√£o
                const interactiveObjects = document.querySelectorAll('[interactive-object]');
                interactiveObjects.forEach(obj => {
                    obj.alreadyTriggeredMobile = false;
                });
                lastCheck = now;
            }
        });
        
        console.log('üì± Detec√ß√£o por orienta√ß√£o do dispositivo ativada');
    }
}

